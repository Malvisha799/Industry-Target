// Page navigation functions
function showHomePage() {
  document.getElementById("homePage").classList.add("active");
  document.getElementById("servicePage").classList.remove("active");
  window.scrollTo(0, 0);
}

function showServicePage(service) {
  const serviceData = serviceDetails[service];
  if (!serviceData) return;

  // Update service page content
  document.getElementById("serviceTitle").textContent = serviceData.title;
  document.getElementById("serviceIcon").className = `icon ${serviceData.icon}`;

  // Update tab contents
  document.getElementById("overview").innerHTML = serviceData.overview;
  document.getElementById("keyFeatures").innerHTML = serviceData.keyFeatures;
  document.getElementById("services").innerHTML = serviceData.services;

  // Show service page
  document.getElementById("homePage").classList.remove("active");
  document.getElementById("servicePage").classList.add("active");
  window.scrollTo(0, 0);
}

// Tab functionality
function initializeTabs() {
  const tabs = document.querySelectorAll(".tab");
  const tabContents = document.querySelectorAll(".tab-content");

  tabs.forEach((tab) => {
    tab.addEventListener("click", () => {
      // Remove active class from all tabs and contents
      tabs.forEach((t) => t.classList.remove("active"));
      tabContents.forEach((c) => c.classList.remove("active"));

      // Add active class to clicked tab and corresponding content
      tab.classList.add("active");
      const tabId = tab.dataset.tab;
      document.getElementById(tabId).classList.add("active");
    });
  });
}

// Initialize the application
document.addEventListener("DOMContentLoaded", () => {
  const cards = document.querySelectorAll(".card");

  // Add hover effects to cards
  cards.forEach((card) => {
    card.addEventListener("mouseenter", () => {
      card.style.transform = "translateY(-8px)";
    });

    card.addEventListener("mouseleave", () => {
      card.style.transform = "translateY(0)";
    });
  });

  // Add click handlers to view details buttons
  cards.forEach((card) => {
    const viewButton = card.querySelector(".view-details");
    viewButton.addEventListener("click", (e) => {
      e.stopPropagation();
      const service = card.dataset.service;
      showServicePage(service);
    });
  });

  // Initialize tabs
  initializeTabs();

  // Intersection Observer for card animations
  const observerOptions = {
    threshold: 0.1,
  };

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.style.opacity = "1";
        entry.target.style.transform = "translateY(0)";
      }
    });
  }, observerOptions);

  // Animate cards on scroll
  cards.forEach((card, index) => {
    card.style.opacity = "0";
    card.style.transform = "translateY(50px)";
    card.style.transition = `all 0.5s ease ${index * 0.1}s`;
    observer.observe(card);
  });

  // Handle browser back/forward buttons
  window.addEventListener("popstate", (e) => {
    if (e.state && e.state.page === "service") {
      showServicePage(e.state.service);
    } else {
      showHomePage();
    }
  });
});

// Add to browser history when navigating to service page
function showServicePage(service) {
  const serviceData = serviceDetails[service];
  if (!serviceData) return;

  // Update service page content
  document.getElementById("serviceTitle").textContent = serviceData.title;
  document.getElementById("serviceIcon").className = `icon ${serviceData.icon}`;

  // Update tab contents
  document.getElementById("overview").innerHTML = serviceData.overview;
  document.getElementById("keyFeatures").innerHTML = serviceData.keyFeatures;
  document.getElementById("services").innerHTML = serviceData.services;

  // Show service page
  document.getElementById("homePage").classList.remove("active");
  document.getElementById("servicePage").classList.add("active");
  window.scrollTo(0, 0);

  // Add to browser history
  history.pushState(
    { page: "service", service: service },
    serviceData.title,
    `#${service}`
  );
}

